String getCardNumber(cardnumber) {
  Map cards = {
    "visa": r"^4[0-9]{12}(?:[0-9]{3})",
    "mastercard": r"^5[1-5][0-9]{14}",
    "amex": r"^3[47][0-9]{13}",
    "discover": r"^6(?:011|5[0-9]{2})[0-9]{12}",
    "hipercard": r"^(606282\d{10}(\d{3})?)|(3841\d{15})",
    "elo":
        r"^((((636368)|(438935)|(504175)|(451416)|(636297))\d{0,10})|((5067)|(4576)|(4011))\d{0,12})",
    "jcb": r"^(?:2131|1800|35\d{3})\d{11}",
    "aura": r"^(5078\d{2})(\d{2})(\d{11})$"
  };

  Map cardLogo = {
    "visa":
        "https://upload.wikimedia.org/wikipedia/commons/thumb/5/5e/Visa_Inc._logo.svg/1280px-Visa_Inc._logo.svg.png",
    "mastercard":
        "https://upload.wikimedia.org/wikipedia/commons/thumb/2/2a/Mastercard-logo.svg/1280px-Mastercard-logo.svg.png",
    "diners": "https://cdn-icons-png.flaticon.com/512/196/196548.png",
    "amex":
        "https://www.pngmart.com/files/13/American-Express-Logo-PNG-File.png",
    "hipercard": "https://logospng.org/wp-content/uploads/hipercard.png",
    "elo":
        "https://logodownload.org/wp-content/uploads/2017/04/elo-logo-1-1.png",
  };

  for (var flag in cards.keys) {
    if (RegExp(cards[flag]).hasMatch(cardnumber)) {
      return cardLogo[flag];
    }
  }
  return "";
}
