List<List<dynamic>> sliceArray(List<dynamic> data) {
  List<List<dynamic>> backup = [];
  int i = 0;
  int length = data.length;

  while (i < length) {
    if (backup.isEmpty || backup[backup.length - 1].length == 1) {
      if (data.elementAtOrNull(i + 1) == null) {
        backup.add([data[i]]);
      } else {
        backup.add([data[i], data[i + 1]]);
      }
      i += 2;
    } else if (backup[backup.length - 1].length == 2) {
      backup.add([data[i]]);
      i++;
    }
  }

  return backup;
}

